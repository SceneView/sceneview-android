#!/usr/bin/env node
/**
 * Generate `src/generated/llms-txt.ts` from the repo root `llms.txt`.
 *
 * The generated module exposes `LLMS_TXT` as a plain TypeScript string
 * constant. This lets the MCP tool handlers run in environments without
 * a filesystem (e.g. Cloudflare Workers for the gateway) while keeping
 * the stdio npm package working unchanged.
 *
 * Search order for the source `llms.txt`:
 *   1. SCENEVIEW_LLMS_TXT env var (absolute path)
 *   2. ../llms.txt (repo root, the canonical location when building from monorepo)
 *   3. ./llms.txt (copied by npm prepare when building the package standalone)
 *
 * Running the script:
 *   node scripts/generate-llms-txt.js
 *
 * It is wired into the `prebuild` npm script so `npm run build` keeps
 * the generated file in sync, and committed to git so tests / gateway
 * imports work without a build step.
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const mcpRoot = resolve(__dirname, "..");

const candidates = [
  process.env.SCENEVIEW_LLMS_TXT,
  resolve(mcpRoot, "..", "llms.txt"),
  resolve(mcpRoot, "llms.txt"),
].filter(Boolean);

let sourcePath = null;
for (const candidate of candidates) {
  if (candidate && existsSync(candidate)) {
    sourcePath = candidate;
    break;
  }
}

if (!sourcePath) {
  console.error(
    "[generate-llms-txt] could not locate llms.txt. Tried:\n  " +
      candidates.join("\n  "),
  );
  process.exit(1);
}

const content = readFileSync(sourcePath, "utf-8");

const outDir = resolve(mcpRoot, "src", "generated");
if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true });
}

const outFile = resolve(outDir, "llms-txt.ts");

const header = [
  "// Auto-generated by scripts/generate-llms-txt.js — DO NOT EDIT BY HAND.",
  "//",
  "// Re-run `node scripts/generate-llms-txt.js` (or `npm run build`) after",
  "// editing the root `llms.txt` to refresh this file.",
  "",
  "/** The full SceneView LLMs API reference, embedded as a string constant. */",
].join("\n");

const body = `export const LLMS_TXT = ${JSON.stringify(content)};\n`;

writeFileSync(outFile, `${header}\n${body}`);

console.log(
  `[generate-llms-txt] wrote ${outFile} (${content.length} chars from ${sourcePath})`,
);
